# Functie om y te berekenen
def bereken_y(x):
    y = 4 * x**3 - 2 * x**2 - 1
    return y

# Testen met verschillende waarden van x
x1 = 1
y1 = bereken_y(x1)
print(f"Voor x = {x1}, is de uitkomst: {y1}")

x2 = 2
y2 = bereken_y(x2)
print(f"Voor x = {x2}, is de uitkomst: {y2}")

x3 = 0
y3 = bereken_y(x3)
print(f"Voor x = {x3}, is de uitkomst: {y3}")

