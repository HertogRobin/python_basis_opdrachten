# Gegeven waarden
c = -12
f = 144

# Conversie van Celsius naar Fahrenheit
fahrenheit = (c * 9/5) + 32

# Conversie van Fahrenheit naar Celsius
celsius = (f - 32) * 5/9

# Toon de resultaten
print(f"{c} graden Celsius is gelijk aan {fahrenheit:.1f} graden Fahrenheit")
print(f"{f} graden Fahrenheit is gelijk aan {celsius:.1f} graden Celcius")
